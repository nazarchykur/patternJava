package com.patternJava.structural.adapter.adapter_2;

public class JavaToDatabaseAdapter extends JavaApp implements Database {
    @Override
    public void insert() {
        saveObject();
    }

    @Override
    public void update() {
        updateObject();
    }

    @Override
    public void select() {
        loadObject();
    }

    @Override
    public void remove() {
        deleteObject();
    }
}
